// UpperAndLower.cpp
#include <iostream>
using namespace std;

int main() {

	char ch = 'A';
	// cin >> ch;
	int b;

	b = ch;

	cout << b << endl;

	if (b >= 65 && b <= 90) {
		cout << "Upper Case" << endl;
	}
	else {
		cout << "Lower Case" << endl;
	}

	if (ch >= 'A' && ch <= 'Z') {
		cout << "Upper Case" << endl;
	}
	else {
		cout << "Lower Case" << endl;
	}

	return 0;
}
















